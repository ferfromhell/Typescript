interface Person {
    firstName: string;
    lastName: string;
}

function greeterInterface(person: Person) {
    return `Hello,  ${person.firstName} ${person.lastName}`;
}

let userInterface = { firstName: "Jane", lastName: "Interface" };

console.log(greeterInterface(userInterface));

